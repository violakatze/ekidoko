/** デフォルト路線色 */
export const DEFAULT_LINE_COLOR = '#888888';

/**
 * 会社名と路線名から路線カラーを返す
 * 優先順位: LINE_COLORS（複合キー）→ COMPANY_COLORS（会社名）→ DEFAULT_LINE_COLOR
 * @param lineName - 路線名（N02_003）
 * @param companyName - 会社名（N02_004）
 * @param useDefault - true の場合は常に DEFAULT_LINE_COLOR を返す（レベル3用）
 */
export const getLineColor = (lineName: string, companyName: string, useDefault = false): string => {
  if (useDefault) return DEFAULT_LINE_COLOR;
  return LINE_COLORS[`${companyName}|${lineName}`] ?? COMPANY_COLORS[companyName] ?? DEFAULT_LINE_COLOR;
};
